name = "Aniket"

print(name[0 : 3])
print(name[-4 : -1]) # start from index +4 all the way till -1 (excluding -1)   numbering starting from backwards

print(name[1:4]) # start from index 1 all the way till 4 (excluding 4)

print(name[:4]) # is same as print(name[0:4])
print(name[1:]) # is same as print(name[1:6])
print(name[1:6])

print(name[1 : 6 : 2]) # nkt